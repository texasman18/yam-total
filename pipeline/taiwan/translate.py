def translate_zh_ko(text, stats=None):
    """ZH_KO 사전 적용 후 남은 한자는 hanja 폴백. 변환 없으면 원문 그대로 반환."""
    if not text:
        return text
    if not CJK_RE.search(text):
        return text  # 한자 없음 — 원문 그대로

    r = text
    for src, dst in _ZH_SORTED:
        if src in r:
            r = r.replace(src, dst)

    # 사전 적용 후에도 한자가 남아있으면 hanja 폴백
    if CJK_RE.search(r):
        if HANJA_AVAILABLE:
            r = hanja.translate(r, "substitution")
            if stats is not None:
                stats["hanja_fallback_used"] += 1
        else:
            if stats is not None:
                stats["hanja_unavailable_残"] += 1

    r = re.sub(r"\s+", " ", r).strip()
    return r


# 주소 행정 접두 (縣市 + 區鄉鎮市) — b 생략 최적화 판정용
_ADMIN_PREFIX_RE = re.compile(r'^\d*\s*[一-鿿]{1,3}[市縣](?:[一-鿿]{1,4}[區鄉鎮市])?')


def dict_hits_beyond_prefix(a):
    """주소 a에서 행정 접두(시·현+구·향·진) 이후 부분에 ZH_KO 사전 히트가 있는지.
    없으면 b는 '시·구 단위만 다른' 것 — 생략 대상 (나머지는 한자 음독뿐이라 표시 가치 낮음)."""
    m = _ADMIN_PREFIX_RE.match(a)
    rest = a[m.end():] if m else a
    if not rest:
        return False
    for src, _ in _ZH_SORTED:
        if src in rest:
            return True
    return False


